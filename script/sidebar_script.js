// nike
const button_nike = document.querySelector('.filter-button-nike');

let count_nike = 0;

button_nike.addEventListener('click', () => {
    count_nike++;

    if(count_nike%2 == 1){
        button_nike.style.backgroundColor = '#E4D4CC';
    }

    else if(count_nike%2 == 0){
        button_nike.style.backgroundColor = '#FFFFFF';
    }
});

